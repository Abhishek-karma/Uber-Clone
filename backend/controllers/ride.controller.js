const rideService = require('../services/ride.service');
const { validationResult } = require('express-validator');
const mapService = require('../services/maps.service');
const { sendMessageToSocketId } = require('../socket');
const rideModel = require('../models/ride.model');


// In ride.controller.js
module.exports.createRide = async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
        return res.status(400).json({ errors: errors.array() });
    }

    const { userId, pickup, destination, vehicleType } = req.body;

    try {
        const ride = await rideService.createRide({ user: req.user._id, pickup, destination, vehicleType });
    
        try {
          const pickupCoordinates = await mapService.getAddressCoordinate (pickup);
          const captainsInRadius = await mapService.getCaptainsInTheRadius(pickupCoordinates.lat, pickupCoordinates.lng, 2); // Use correct keys
    
          ride.otp = ""; // Generate OTP here if needed
    
          const rideWithUser = await rideModel.findOne({ _id: ride._id }).populate('user'); // Asynchronous call
    
          res.status(201).json(rideWithUser); // Send response after populating user
    
          captainsInRadius.forEach(captain => {
            sendMessageToSocketId(captain.socketId, {
              event: 'new-ride',
              data: rideWithUser
            });
          });
          
    

        } catch (innerError) {
            console.error("Error within ride creation process:", innerError);
            //Important: If geocoding or captain search fails, you might want to consider rolling back the ride creation
            //For now, we will simply log the error and send a message to the user that we are facing issues.
            return res.status(500).json({ message: "An error occurred while finding captains. Please try again later." });
        }

    } catch (error) {
        console.error("Error creating ride:", error);
        res.status(error.statusCode || 500).json({ message: error.message || "An error occurred." });
    }
};

module.exports.getFare = async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
        return res.status(400).json({ errors: errors.array() });
    }

    const { pickup, destination } = req.query;

    try {
        const fare = await rideService.getFare(pickup, destination);
        return res.status(200).json(fare);
    } catch (err) {
        return res.status(500).json({ message: err.message });
    }
} 

module.exports.confirmRide = async (req, res) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
        return res.status(400).json({ errors: errors.array() });
    }

    const { rideId } = req.body;

    try {
        const ride = await rideService.confirmRide({ rideId, captain: req.captain });

        sendMessageToSocketId(ride.user.socketId, {
            event: 'ride-confirmed',
            data: ride
        })

        return res.status(200).json(ride);
    } catch (err) {

        console.log(err);
        return res.status(500).json({ message: err.message });
    }
}

